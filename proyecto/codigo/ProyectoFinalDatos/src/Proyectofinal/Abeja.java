package Proyectofinal;

public class Abeja {
    private double X;
    private double Y;
    private int num;
    
    public Abeja(double X, double Y, int num){
        this.X = X;
        this.Y = Y;
        this.num = num;
    }

    public double getX(){
        return X;
    }
    
    public double getY(){
        return Y;
    }
    
    public int getNUM(){
        return num;
    }
    
}
